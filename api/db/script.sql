CREATE DATABASE IF NOT EXISTS concessionaria;
USE concessionaria;
CREATE TABLE IF NOT EXISTS carros (
  id INT(11) AUTO_INCREMENT,
  placa VARCHAR (7),
  marca VARCHAR(100),
  modelo VARCHAR(100),
  ano INT(4),
  preco DECIMAL(10, 2),
  PRIMARY KEY (id)
);

